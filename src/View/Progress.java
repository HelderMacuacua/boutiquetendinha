    class pbThead extends Thread{
        JProgressBar pbar;
        
        pbThead(JProgressBar pbar){
            pbar=progress;
            
        }
    public  void run(){
        int min=0;
        int max=100;
        progress.setMaximum(min);
        progress.setMaximum(max);
        progress.setValue(0);
        
        
        
        try {
        for(int i=min;i<=max;i++){
            progress.setValue(i);
            
                sleep(100);
                progress.setValue(i);
                txtpin.setText(String.valueOf(i)+"%");
                if (progress.getValue()<=35) {
                        lblcarregar.setText("  Carregando o Sistema...");
                    }else if (progress.getValue()<=70) {
                        lblcarregar.setText("Carregando Banco de Dados...");
                    }else{
                        lblcarregar.setText("O Sistema estÃ¡ Sendo Inicializado...");
                    }
                if(progress.getValue()==100){
                    setVisible(false);
                    dispose();
                   
                }
            } 
            
        }catch (InterruptedException ex) {
                Logger.getLogger(Progress.class.getName()).log(Level.SEVERE, null, ex);
            }
        ViewTelaPrincipal principal=new ViewTelaPrincipal();
       principal.setVisible(true);
        
        }
    
        
    }

