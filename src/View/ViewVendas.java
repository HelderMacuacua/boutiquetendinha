    private void limparDados(){
        this.jtfDesconto.setText("");
        this.jtfQuantidade.setText("");
        this.jtfValorTota.setText(""); 
        DefaultTableModel model=(DefaultTableModel) this.jtVendas.getModel();
        model.setRowCount(0);
    }  
    
    private void somarValorTotalDeVendas(){
      double valor,soma=0;
      int contador=this.jtVendas.getRowCount();
        for (int i = 0; i < contador; i++) {
            valor=(double) this.jtVendas.getValueAt(i, 4);
            soma=soma+valor;
        } 
        this.jtfValorTota.setText(String.valueOf(soma)); 
        aplicarDesconto();
} 
    private void aplicarDesconto(){ 
        
        if(this.jtfDesconto.getText().equals("")){
            this.jtfDesconto.setText(String.valueOf(0));
        }else{
        this.jtfValorTota.setText(String.valueOf(Double.parseDouble(this.jtfValorTota.getText())-Double.parseDouble(this.jtfDesconto.getText())));
            
        }
    } 
    private List<ModeloProduto> pegarTodosProdutosQueOClienteAdicionouNasVendas(){
        List<ModeloProduto> listaDeProdutos=new ArrayList<>();
        DefaultTableModel modelo=(DefaultTableModel) this.jtVendas.getModel();
        int quantidadeDeprodutos=this.jtVendas.getRowCount();
        for (int i = 0; i < quantidadeDeprodutos; i++) {
           modeloProduto=new ModeloProduto();
           modeloProduto.setIdProduto(Integer.parseInt(String.valueOf(modelo.getValueAt(i, 0))));
           modeloProduto.setDescricao(String.valueOf(modelo.getValueAt(i, 1)));
           
           listaDeProdutos.add(modeloProduto);
        }
        
        return listaDeProdutos;
    }

