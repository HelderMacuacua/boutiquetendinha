    private void jbAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAdicionarActionPerformed
        // TODO add your handling code here:
        if (this.jtfQuantidade.getText().equalsIgnoreCase("")) { 
            this.setEnabled(false);
            new ViewPreencherTodosCamposAdicionarQuantidade(this).setVisible(true);
        } else { 
   modeloProduto=(ModeloProduto) this.jcbProdutoss.getSelectedItem();
   DefaultTableModel modelo=(DefaultTableModel) this.jtVendas.getModel();
   double quantidade = 0;
   quantidade=Double.parseDouble(jtfQuantidade.getText());
   modelo.addRow(new Object[]{
       modeloProduto.getIdProduto(),
       modeloProduto.getDescricao(),
       jtfQuantidade.getText(),
       modeloProduto.getValor(),
       (quantidade*modeloProduto.getValor())
       
   });  
   somarValorTotalDeVendas();
        }
  
        
    }//GEN-LAST:event_jbAdicionarActionPerformed

    private void jbRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRemoverActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo=(DefaultTableModel) this.jtVendas.getModel();
        try {
         
        int linha=this.jtVendas.getSelectedRow();
        modelo.removeRow(linha);
        } catch (Exception e) { 
            e.printStackTrace();
            this.setEnabled(false);
            new ViewSelecionarProdutoAdicionado(this).setVisible(true);
        }
       
    }//GEN-LAST:event_jbRemoverActionPerformed

